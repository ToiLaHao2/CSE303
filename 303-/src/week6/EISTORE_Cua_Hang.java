package week6;

import java.io.*;
import java.util.*;;

class EISTORE_Cua_Hang {

	public static void main(String[] args) {
		int numberTypes = ni();
        int money = ni();

        int[] values = new int[numberTypes];
        for (int i = 0; i < numberTypes; i++) {
            values[i] = ni();
        }

        int result = minItems(numberTypes, money, values);
        System.out.println(result);
	}
	
	 public static int minItems(int numberTypes, int money, int[] values) {
	        int[] dp = new int[money + 1];
	        Arrays.fill(dp, Integer.MAX_VALUE);
	        dp[0] = 0;

	        for (int value : values) {
	            for (int j = value; j <= money; j++) {
	                if (dp[j - value] != Integer.MAX_VALUE) {
	                    dp[j] = Math.min(dp[j], dp[j - value] + 1);
	                }
	            }
	        }

	        if (dp[money] == Integer.MAX_VALUE) {
	        	return -1;
	        } else {
	        	return dp[money];
	        }
	        
	    }

	static InputStream is = System.in;
	static byte[] inbuf = new byte[1 << 24];
	static int lenbuf = 0, ptrbuf = 0;

	static int readByte() {
		if (lenbuf == -1)
			throw new InputMismatchException();
		if (ptrbuf >= lenbuf) {
			ptrbuf = 0;
			try {
				lenbuf = is.read(inbuf);
			} catch (IOException e) {
				throw new InputMismatchException();
			}
			if (lenbuf <= 0)
				return -1;
		}
		return inbuf[ptrbuf++];
	}

	static boolean isSpaceChar(int c) {
		return !(c >= 33 && c <= 126);
	}

	static int skip() {
		int b;
		while ((b = readByte()) != -1 && isSpaceChar(b))
			;
		return b;
	}

	static double nd() {
		return Double.parseDouble(ns());
	}

	static char nc() {
		return (char) skip();
	}

	static String ns() {
		int b = skip();
		StringBuilder sb = new StringBuilder();
		while (!(isSpaceChar(b))) {
			sb.appendCodePoint(b);
			b = readByte();
		}
		return sb.toString();
	}

	static char[] ns(int n) {
		char[] buf = new char[n];
		int b = skip(), p = 0;
		while (p < n && !(isSpaceChar(b))) {
			buf[p++] = (char) b;
			b = readByte();
		}
		return n == p ? buf : Arrays.copyOf(buf, p);
	}

	static int ni() {
		int num = 0, b;
		boolean minus = false;
		while ((b = readByte()) != -1 && !((b >= '0' && b <= '9') || b == '-'))
			;
		if (b == '-') {
			minus = true;
			b = readByte();
		}
		while (true) {
			if (b >= '0' && b <= '9') {
				num = num * 10 + (b - '0');
			} else {
				return minus ? -num : num;
			}
			b = readByte();
		}
	}

	static long nl() {
		long num = 0;
		int b;
		boolean minus = false;
		while ((b = readByte()) != -1 && !((b >= '0' && b <= '9') || b == '-'))
			;
		if (b == '-') {
			minus = true;
			b = readByte();
		}
		while (true) {
			if (b >= '0' && b <= '9') {
				num = num * 10 + (b - '0');
			} else {
				return minus ? -num : num;
			}
			b = readByte();
		}
	}
}
